#include "fdf.h"

void		free_map(t_map **map)
{
	t_map *m;

	m = *map;
	ft_free_iint(&(m->coords));
	free(m->colors);
}

void		free_env(t_env *e)
{
	free((*e).pixel_img);
	free_map(&((*e).map));
}
