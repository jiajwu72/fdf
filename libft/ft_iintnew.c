#include "libft.h"

int					**ft_iintnew(int a, int b)
{
	int		i;
	int		**s;

	i = -1;
	s = malloc(sizeof(int *) * (a + 1));
	while (++i < a)
		s[i] = ft_intnew(b);
	s[i] = NULL;
	return (s);
}
